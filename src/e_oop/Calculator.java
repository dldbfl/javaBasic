package e_oop;

public class Calculator {
	
	int plus(int i, int j){
   	 	return i+j;
	   }
	
	long minus(long i, long j){
		return i-j;
	   }
	long multiplication(long i, long j){
	   	return i*j;
	   }
	long divide(long i, long j){
	   	return i/j;
	   }
	long remainder(long i, long j){
	   	return i%j;
	   }
	

}
